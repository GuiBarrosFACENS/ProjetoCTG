    private void adicionar() throws SQLException {

        String sql = "INSERT INTO cadastro_clientes (id,nome, cpf, endereco,numero_endereco,nascimento,celular)"
                + " VALUES (?,?,?,?,?,?,?);";

        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtCpf.getText());
            pst.setString(3, txtend.getText());
            pst.setString(4, txtNumero.getText());
            pst.setString(5, txtNascimento.getText());
            pst.setString(6, txtCel.getText());
            pst.executeQuery();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "é aqui"+e);
        }

    }

